#include<iostream>

using namespace std;

int main()

{

    int width,height,area,perimeter;

    cout<<"Enter Length of Rectangle = ";

    cin>>width;

    cout<<"\nEnter Breadth of Rectangle = ";

    cin>>height;

    area=height*width;

    cout<<"\nArea of Rectangle = "<<area<<endl;

    perimeter=2*(height+width);

cout<<"\nPerimeter of rectangle are = "<<perimeter<<endl;

return 0;

}

